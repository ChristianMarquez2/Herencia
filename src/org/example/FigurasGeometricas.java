package org.example;

public class FigurasGeometricas {
    String nombre;
    int lados;
    public FigurasGeometricas(String nombre, int lados) {}

    public FigurasGeometricas() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getLados() {
        return lados;
    }

    public void setLados(int lados) {
        this.lados = lados;
    }

}
